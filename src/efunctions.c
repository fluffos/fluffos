void
f_member_array(num_arg, instruction)
int num_arg, instruction;
{
  struct vector *v;

  v = sp->u.vec;
  check_for_destr(v);
  for (i=0; i < v->size; i++)
  {
    if (v->item[i].type != (sp-1)->type)
      continue;
    switch((sp-1)->type)
      {
      case T_STRING:
	if (strcmp((sp-1)->u.string, v->item[i].u.string) == 0)
	  break;
	continue;
      case T_POINTER:
	if ((sp-1)->u.vec == v->item[i].u.vec)
	  break;
	continue;
      case T_OBJECT:
	if ((sp-1)->u.ob == v->item[i].u.ob)
	  break;
	continue;
      case T_NUMBER:
	if ((sp-1)->u.number == v->item[i].u.number)
	  break;
	continue;
      case T_MAPPING:
	if ((sp-1)->u.map == v->item[i].u.map)
	  break;
	continue;
      default:
	fatal("Bad type to member_array(): %d\n", (sp-1)->type);
      }
    break;
  }
  if (i == v->size)
    i = -1;			/* Return -1 for failure */
  pop_n_elems(2);
  push_number(i);
}

