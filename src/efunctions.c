void
f_replace_string(num_arg, instruction)
    int num_arg, instruction;
{
    int plen, rlen, dlen;
    char *src, *pattern, *replace, *dst1, *dst2;

    if (IS_ZERO(sp)) { /* first and second args checked elsewhere */
    	bad_arg(3, F_REPLACE_STRING);
    	pop_n_elems(3);
    	return;
    }
    src = (sp-2)->u.string;
    pattern = (sp-1)->u.string;
    replace = sp->u.string;
    dst2 = dst1 = (char *)DMALLOC(max_string_length, 32, "f_replace_string");

    plen = strlen(pattern);
    rlen = strlen(replace);
    dlen = 0;

    if (plen == 0) {
	strcpy(dst2, src);
    } else {
	while (*src != '\0') {
	    if (strncmp(src, pattern, plen) == 0) {
		if (rlen != 0) {
		    if (max_string_length - dlen <= rlen) {
			pop_n_elems(3);
			push_svalue(&const0u);
			FREE(dst1);
			return;
		    }
		    strncpy(dst2, replace, rlen);
		    dst2 += rlen;
		    dlen += rlen;
		}
		src += plen;
	    } else {
		if (max_string_length - dlen <= 1) {
		    pop_n_elems(3);
		    push_svalue(&const0u);
		    FREE(dst1);
		    return;
		}
		*dst2++ = *src++;
		dlen++;
	    }
	}
	*dst2 = '\0';
    }
    pop_n_elems(3);
    push_string(dst1, STRING_MALLOC);
    FREE(dst1);
}

